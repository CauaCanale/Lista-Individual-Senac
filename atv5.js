// 5. Escreva um programa que converta graus Celsius para Fahrenheit. (#6)

// Solicita a temperatura em Celsius ao usuário
let celsius = parseInt(prompt("Digite a temperatura em Celsius:"));


// Converte Celsius para Fahrenheit
let fahrenheit = (celsius * 9/5) + 32;

// Exibe o resultado
alert("A temperatura em Fahrenheit é: " + fahrenheit);